# PowerShell script to forcibly remove node_modules and package-lock.json on Windows

# Stop any running node processes (optional, uncomment if needed)
# Get-Process node | Stop-Process -Force

# Remove node_modules folder forcibly
Write-Host "Removing node_modules folder..."
Remove-Item -LiteralPath "node_modules" -Recurse -Force -ErrorAction SilentlyContinue

# Remove package-lock.json file
Write-Host "Removing package-lock.json file..."
Remove-Item -LiteralPath "package-lock.json" -Force -ErrorAction SilentlyContinue

Write-Host "Cleanup complete. Please run 'npm install' to reinstall dependencies."
